function checkContact() {
  let contact = document.getElementById("yourContactNo").value;
  console.log(contact);
  if (!isNaN(contact)) {
    document.getElementById("warning_Contact").style.display = "none";
    if(contact.length<10){
      document.getElementById("warning_Contact2").style.display = "block";
      return false;
    }
    else{
      document.getElementById("warning_Contact2").style.display = "none";
      return true;
    }
  }
  else {
    document.getElementById("warning_Contact").style.display = "block";
    return false;
  }
}

function checkRent() {
  let rent = document.getElementById("yourHouseRent").value;
  if ((rent > 1000) && (rent < 100000)) {
    document.getElementById("warning_Rent").style.display = "none";
    return true;
  }
  else {
    document.getElementById("warning_Rent").style.display = "block";
    return false;
  }
}

function checkName() {
  let name = document.getElementById("yourName").value;
  let letters = /^[A-Za-z]+$/;
  if (name.match(letters)) {
    document.getElementById("warning_name").style.display = "none";
    return true;
  }
  else if (name.includes(" ")) {
      document.getElementById("warning_name").style.display = "none";
      return true;
    }
  else {
      document.getElementById("warning_name").style.display = "block";
      return false;
    }
}

function checkEmail() {
  let email = document.getElementById("yourEmail").value;
  let emailcheck = /^\w+([\.-]?\w+)@\w+([\.-]?\w+)(\.\w{2,3})+$/;
  if (email.match(emailcheck)) {
    document.getElementById("warning_Email").style.display = "none";
    return true;
  }
  else {
    document.getElementById("warning_Email").style.display = "block";
    return false;
  }
}

function checkPassword() {
  let pass = document.getElementById("yourPassword").value;

  if (pass.length > 6) {
    document.getElementById("warning_password").style.display = "none";
    if (pass.length > 11) {
      document.getElementById("warning_password2").style.display = "block";
      return false;
    }
    else {
      document.getElementById("warning_password2").style.display = "none";
      return true;
    }
  }
  else {
    document.getElementById("warning_password").style.display = "block";
    return false;
  }
}

